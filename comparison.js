// Declare two favorite numbers
let firstFavNumb = 3;
let secondFavNumb = 22;

// Perform comparison operations
console.log(firstFavNumb > secondFavNumb);  // Greater than
console.log(firstFavNumb < secondFavNumb);  // Less than
console.log(firstFavNumb >= secondFavNumb); // Greater than or equal
console.log(firstFavNumb <= secondFavNumb); // Less than or equal
console.log(firstFavNumb === secondFavNumb); // Strict equality
console.log(firstFavNumb == secondFavNumb);  // Loose equality
console.log(firstFavNumb !== secondFavNumb); // Strict inequality
console.log(firstFavNumb != secondFavNumb);  // Loose inequality
